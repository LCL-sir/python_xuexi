# 变量
money = 50;
print(money)

money = money - 10;
print(money)

money += 20;
print(money)

# 判断数据类型
print(type(money));

# 转换数据类型
lei0 = 12;
lei0 = str(lei0);
print(type(lei0))

# 结束字符串范围
zifu = "\"12313";
print(zifu)

# 字符串拼接
name0 = "龙右";
age0 = 18;

print(f"名字:{name0},年龄:{age0}");

# 输入
name1 = input();
print(name1)

name2 = input("请输入你的名字"); #input 里面默认接受的都是str 类型的
